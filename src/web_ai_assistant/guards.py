"""Архитектурные «красные линии» и эскалации (доклад, раздел про делегирование).

Эти проверки выполняются ДО обращения к LLM — это и есть архитектурное
ограничение, а не декларация в промпте.
"""

from __future__ import annotations

import re

# ---------------------------------------------------------------------------
# Red zone — запрещено архитектурно (выставление оценок, написание работ)
# ---------------------------------------------------------------------------
RED_ZONE_PATTERNS: tuple[str, ...] = (
    r"поставь\s*(мне)?\s*(оценк|балл)",
    r"оцени\s+мою\s+работу",
    r"напиши\s+(за\s+меня|мне)\s+(лабораторн|курсов|работу|код)",
    r"сделай\s+(за\s+меня|мне)\s+(лабораторн|курсов|задание)",
    r"grade\s+(my|me)",
    r"write\s+my\s+(assignment|lab|homework)",
)
RED_ZONE_RE = re.compile("|".join(RED_ZONE_PATTERNS), re.IGNORECASE)

# ---------------------------------------------------------------------------
# Эскалация на преподавателя — новая тема (оранжевая зона)
# ---------------------------------------------------------------------------
ESCALATION_TRIGGERS: tuple[str, ...] = (
    "объясни мне новую тему",
    "это новая тема",
    "впервые сталкиваюсь",
    "explain a new topic",
)


def is_red_zone(question: str) -> bool:
    """True если запрос пытается обойти красную линию (оценка/чужая работа)."""
    return bool(RED_ZONE_RE.search(question))


def is_escalation(question: str) -> bool:
    """True если студент явно сигнализирует о новой теме (нужен преподаватель)."""
    ql = question.lower()
    return any(t in ql for t in ESCALATION_TRIGGERS)


